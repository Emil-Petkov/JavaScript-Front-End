let num = [11, 22, 33, 44, 55, 66, 77];

console.log(num.length); //7
console.log(num.push(88)); //8 elements -> return length of array
console.log(num); //[11, 22, 33, 44, 55, 66, 77, 88]
console.log(num.push(99, 1010, 1111, 1212)); ////12 elements -> return length of array
console.log(num); //[11, 22, 33, 44, 55, 66, 77, 88, 99, 1010, 1111, 1212]

