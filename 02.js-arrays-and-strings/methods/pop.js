let numbers = [11, 22, 33, 44, 55, 66, 77];

console.log(numbers.length); //7
console.log(numbers.pop()); //77 -> return value
console.log(numbers.length); //6
console.log(numbers); //[11, 22, 33, 44, 55, 66]

